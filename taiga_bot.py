logging.basicConfig(level=logging.INFO)

client = Groq(api_key=GROQ_API_KEY)

# Хранит историю чата для каждого пользователя
chat_histories = {}

MAX_HISTORY = 40  # последних сообщений (20 пар)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    chat_id = update.effective_chat.id
    chat_histories[chat_id] = []
    await update.message.reply_text(
        "Тайга ждёт тебя...\n\n"
        "*Обычный вечер. Тайга сидит на диване с Чернышом на коленях, делает вид что читает.*\n\n"
        "«Ты опоздал,» — *бросает она, не отрываясь от книги,* «Ужин остынет.»\n\n"
        "«…Я его оставила тебе. Не потому что хотела. Просто много сделала.»",
        parse_mode="Markdown"
    )


async def reset(update: Update, context: ContextTypes.DEFAULT_TYPE):
    chat_id = update.effective_chat.id
    chat_histories[chat_id] = []
    await update.message.reply_text("История сброшена. Новая сцена началась.")


async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    chat_id = update.effective_chat.id
    user_message = update.message.text

    if chat_id not in chat_histories:
        chat_histories[chat_id] = []

    history = chat_histories[chat_id]
    history.append({"role": "user", "content": user_message})

    # Обрезаем историю
    if len(history) > MAX_HISTORY:
        history = history[-MAX_HISTORY:]
        chat_histories[chat_id] = history

    try:
        response = client.chat.completions.create(
            model=MODEL,
            messages=[
                {"role": "system", "content": SYSTEM_PROMPT},
                *history
            ],
            max_tokens=1024,
            temperature=0.85,
        )

        reply = response.choices[0].message.content
        history.append({"role": "assistant", "content": reply})

        await update.message.reply_text(reply, parse_mode="Markdown")

    except Exception as e:
        logging.error(f"Ошибка: {e}")
        await update.message.reply_text("Что-то пошло не так... попробуй ещё раз.")


def main():
    app = ApplicationBuilder().token(TELEGRAM_TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("reset", reset))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    print("Бот запущен! Напиши /start в Телеге.")
    app.run_polling()


if __name__ == "__main__":
    main()